package zuoye26;

public class zuoye1 {
//1 定义一个数组{8,51,63,21,47,12} 要求正着输出所有元素及反着输出所有元素
	public static void main(String[] args) {
		int[] arr={8,15,63,21,47,12};
		
		for(int a =0;a<=5;a++){
			System.out.print(arr[a]+"\t");
		}
		System.out.println();
		
		for(int b=5;b>=0;b--){
			System.out.print(arr[b]+"\t");
		}
		System.out.println();
		
		
		
		
		
	}
	
	
	
	
	
	
}
